void fillTableNguoiHoc()
{
    DefaultTableModel model = (DefaultTableModel) tblNguoiHoc.getModel();
    model.setRowCount(0);
    try {
        String keyword = txtSearch.getText();
        List<NguoiHoc> list = nhDao.selectByKeyWord(keyword);
        for(NguoiHoc nh : list)
        {
            Object[] row = 
            {
              nh.getMaNH(),nh.getHoTen(),nh.isGioiTinh() ? "Nam" : "Nữ",
              nh.getNgaySinh(),nh.getDienThoai(),nh.getEmail(),
              nh.getMaNV(),XDate.toString(nh.getNgayDK(),"MM/dd/yyyy")
            };
            model.addRow(row);
        }
    } catch (Exception e) {
        MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
    }
}
void setForm(NguoiHoc model)
    {
        txtMaNguoiHoc.setText(model.getMaNH());
        txtHoTen.setText(model.getHoTen());
        boolean gt = model.isGioiTinh();
        if(gt == true)
        {
            rdoNam.setSelected(true);
        }else 
            rdoNu.setSelected(true);
        txtNgaySinh.setText(XDate.toString(model.getNgaySinh(), "MM/dd/yyyy"));
        txtDienThoai.setText(model.getDienThoai());
        txtEmail.setText(model.getEmail());
        txtGhiChu.setText(model.getGhiChu());
    }

    
    NguoiHoc getForm()
    {
        NguoiHoc nh = new NguoiHoc();
        nh.setMaNH(txtMaNguoiHoc.getText());
        nh.setHoTen(txtHoTen.getText());
        boolean gt;
        if(rdoNam.isSelected())
        {
            gt = true;
        } else gt = false;
        nh.setGioiTinh(gt);
        nh.setNgaySinh(XDate.toDate(txtNgaySinh.getText(), "MM/dd/yyyy"));
        nh.setDienThoai(txtDienThoai.getText());
        nh.setEmail(txtEmail.getText());
        nh.setGhiChu(txtGhiChu.getText());
        nh.setMaNV(Auth.user.getMaNV());
        nh.setNgayDK(XDate.now());
       return nh;
    }
    void updateStatus()
    {
        boolean edit = this.row >= 0;
        boolean first = this.row == 0;
        boolean last = this.row == tblNguoiHoc.getRowCount() - 1;
        txtMaNguoiHoc.setEditable(!edit);
        //khi insert thì k update, delete
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
    
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
    void edit()
    {
        try 
        {
            String maNH = (String) tblNguoiHoc.getValueAt(this.row, 0);
            NguoiHoc nh = nhDao.selectById(maNH);
            if(nh != null)
            {
                this.setForm(nh);
                this.updateStatus();
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    void clear()
    {
        NguoiHoc nh = new NguoiHoc();
        nh.setMaNV(Auth.user.getMaNV());
        nh.setNgayDK(XDate.now());
        this.setForm(nh);
    }
    void delete()
    {
        if(MsgBox.confirm(this, "Bạn thực sự muốn xóa người học này?"))
        {
            String maNH = txtMaNguoiHoc.getText();
            try {
                nhDao.delete(maNH);
                this.fillTableNguoiHoc();
                this.clear();
                MsgBox.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Xóa thất bại!");
            }
        }
    }
    void update()
    {
        NguoiHoc nh = getForm();
        try {
            nhDao.update(nh);
            this.fillTableNguoiHoc();
            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }
    void insert()
    {
        NguoiHoc nh = getForm();
        try {
            nhDao.insert(nh);
            this.fillTableNguoiHoc();
            this.clear();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }
    void first()
    {
        row = 0;
        edit();
    }
    void prev()
    {
        if(row > 0)
        {
            row--;
            edit();
        }
    }
    void next()
    {
        if(row < tblNguoiHoc.getRowCount() - 1)
        {
            row++;
            edit();
        }
    }
    void last()
    {
        row = tblNguoiHoc.getRowCount() - 1;
        edit();
    }
    
